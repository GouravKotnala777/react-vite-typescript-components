function updateDots(m:number, arr:string[]) {
  const t:string[] = [];
  arr.map((item, index) => {
    const a = item.split(",");
    const b = a.map(i => Number(i)+m).join(",");
    t.push(b);
  });

  return(t);
};
function moveDotsLeft(m:number, arr:string[]) {
  const t:string[] = [];
  arr.map((item, index) => {
    const a = item.split(",");
    const b = `${Number(a[0])+m},${Number(a[1])-m}`;
    t.push(b);
  });

  return(t);
};


